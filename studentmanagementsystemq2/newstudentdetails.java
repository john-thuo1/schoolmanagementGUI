package studentmanagementsystemq2;

// The class initializes variables needed a student enters his/her details prior to submission of application.
// The details especially grades, are used to determine if the student is admitted or not and the course in which the
// the student is admitted
public class newstudentdetails {
    // These are the fields required when a student enters his/her details.
    public String FullName; // Column FullName of the SQL Student_info
    public String Email; // Column Email of the SQL Student_info
    public String RollNo; // Column RollNo of the SQL Student_info
    public String Grade; // Column Grade of the SQL Student_info
    public String Year; // Column Year of the SQL Student_info
    public String RegistrationNumber; // Column RegistrationNumber of the SQL Student_info
    public String CourseAssigned; // Column CourseAssigned of the SQL Student_info

}
